    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user = auth()->user();
        $createdata = transaction::join('users', 'transactions.id_user', '=', 'users.id')->join('buyers', 'transactions.id_buyer', '=', 'buyers.id')->select('transactions.*','buyers.nama as nama','buyers.from as from','buyers.no_hp as no_hp','buyers.email as email', 'users.name as name_created_by')->where('transactions.deleted', '=', null)->where('buyers.username', '=', "U".$user->id)
                ->latest()->paginate(10);
        return view('user.purchased',compact('createdata'));
    }

